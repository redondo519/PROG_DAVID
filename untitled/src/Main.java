public class Main {
    public static void main(String[] args) {
        boolean booleano = false;
        char caracter = 'A';
        byte enteroOcho = 127;
        short enteroCorto = -14200;
        int entero = 12;
        long enteroLargo = 1232123212345L;
        float decimal = 123.301F;
        double decimalLargo = 123.30101102304;
        System.out.println("Variable boolean:" + booleano );
        System.out.println("Variable char:" + caracter);
        System.out.println("Variable byte:" + enteroOcho);
        System.out.println("Variable short:" + enteroCorto);
        System.out.println("Variable int: " + entero );
        System.out.println("Variable long:" + enteroLargo);
        System.out.println("Variable float:" + decimal);
        System.out.println("Variable double:" + decimalLargo);
    }

}