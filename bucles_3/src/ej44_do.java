public class ej44_do {
    public static void main(String[] args) {

        //Declaracion de variable entera para introducir el primer numero de la cadena descendente
        int i = 320;
        do{
            System.out.println(i); //Mostramos i
            i-=20; //Restamos 20 a i
        }while(i >= 160);

    }
}